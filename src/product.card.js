const categoryType = document.querySelector('#category');
const categoryId      = categoryType.value;

const productBlock = document.querySelector('.product-block');

const products = data[categoryId];

function addProductToContainer(product) {
    const {image, caption, dateAdded} = product;

    const cardProduct = document.createElement('div');
    cardProduct.classList.add('cardProduct');

    const imageElement = document.createElement('img');
    imageElement.src   = image;
    imageElement.classList.add('image');

    const captionElement       = document.createElement('span');
    captionElement.textContent = caption;

    const dateElement       = document.createElement('span');
    dateElement.textContent = formatDate(dateAdded);

    const buttonElement       = document.createElement('button');
    buttonElement.textContent = 'Купить';
    buttonElement.classList.add('btn-buy');

    cardProduct.appendChild(imageElement);
    cardProduct.appendChild(captionElement);
    cardProduct.appendChild(dateElement);
    cardProduct.appendChild(buttonElement);

    productBlock.appendChild(cardProduct);
}

function formatDate(date) {
    const options = {
        weekday : 'long',
        week    : 'numeric',
        month   : 'long',
        year    : 'numeric',
    };
    return date.toLocaleDateString('ru-RU', options);
}

products.forEach(addProductToContainer);

